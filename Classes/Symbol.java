package Classes;

public class Symbol 
{
    // Attributes
    private int id;

    // Constructor
    public Symbol(int id)
    {
        this.setId(id);
    }

    // Methods
    public int getId() 
    { 
        return this.id; 
    }

    public void setId(int id)
    {
        this.id = id;
    }
}
